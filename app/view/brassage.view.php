    <h2 class="etapetext">Etape 2</h2>
    <p class="pbras">Ensuite, c’est l’étape d’empâtage : on mélange le malt concassé avec de l’eau chaude.</p>
    <p class="pbras">C’est là que les enzymes transforment l’amidon en sucres fermentés cibles. </p>
    </div>
