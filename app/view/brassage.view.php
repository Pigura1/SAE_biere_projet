<h1>Le fonctionnement du brassage</h1>
<div id="brassage">
    <img src="public/images/malt.png"/>
    <p>En premier, on commence avec le concassage où l’on broie le malt pour libérer les sucres qui nourriront les levures. </p>
    <img src="public/images/concassage1.jpg"/>
    <img src="public/images/concassage2.png"/>
    <p>Ensuite, c’est l’étape d’empâtage : on mélange le malt concassé avec de l’eau chaude.</p>
    <img src="public/images/maltverseavant.jpeg"/>
    <p>C’est là que les enzymes transforment l’amidon en sucres fermentescibles. </p>
    <img src="public/images/maltverse.png"/>
    <p>Par la suite il faut filtrer ce mélange appelé moût, pour le séparer des résidus solides, qu’on appelle les drêches. </p>
    <img src="public/images/filtrage.png"/>
    <p>Puis vient l’ébullition : on stérilise le moût et on ajoute notre hibiscus, notre rooibos et le houblon qui va apporter l’amertume, les arômes et aide à la conservation.</p>
    <img src="public/images/ebullition.png"/>
    <p>Après ça, on refroidit rapidement le moût pour pouvoir ajouter les levures sans les tuer. Celles-ci vont fermenter pendant plusieurs jours afin que les levures transforment les sucres en alcool et en gaz. </p>
    <img src="public/images/densite1.JPG"/>
    <img src="public/images/densite2.jpeg"/>
    <p>On a suivi cette étape de près jour après jour en mesurant la densité du moût jusqu’au jour de mise en bouteille où l’on a ajouté un peu de sucre pour relancer une fermentation en bouteille et obtenir une bière naturellement pétillante.</p>
    <img src="public/images/bouteilles.JPG"/>
</div>