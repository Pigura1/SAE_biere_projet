    <div id="etape4">
    <h2 class="etapetext">Etape 3</h2>
    <p class="pbras">Par la suite il faut filtrer ce mélange appelé moût, pour le séparer des résidus solides, qu’on appelle les drêches. </p>
    </div>
    <div id="imgetape4">
    <img class="imgbras" src="public/images/filtrage1.png"/>
    </div>
    <div id="etape5">
    <h2 class="etapetext">Etape 4</h2>
    <p class="pbras">Puis vient l’ébullition : on stérilise le moût et on ajoute l'hibiscus, le rooibos et le houblon qui va apporter l’amertume et les arômes en aidant à la conservation. 
    <p class="pbras">Le moût est refroidit rapidement pour pouvoir ajouter les levures sans les tuer. Celles-ci vont fermenter pendant plusieurs jours afin que les levures transforment les sucres en alcool et en gaz.</p>
    </div>
    <div id="imgetape5">
    <img class="imgbras" src="public/images/ebullition.png"/>
    </div>
    <div id="etape6">
    <h2 class="etapetext">Etape 5</h2>
    <p class="pbras">Le groupe GB a suivi cette étape de près jour après jour en mesurant la densité du moût jusqu’au jour de mise en bouteille où ils ont ajouté un peu de sucre pour relancer une fermentation en bouteille et obtenir une bière naturellement pétillante.</p>
