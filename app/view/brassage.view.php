<h1>Le processus du brassage de Cyb3r Heaven</h1>
<div id="brassage">
    
    <h3>Voici le malt, l'ingrédient de base qui va servir au brassage de notre bière.</h3>
    <img src="public/images/malt.png"/>

    <h2>Etape 1</h2>
    <p>En premier, on commence avec le concassage où l’on broie le malt pour libérer les sucres qui nourriront les levures. </p>
    <img src="public/images/concassage1.jpg"/>
    <img src="public/images/concassage2.png"/>

    <h2>Etape 2</h2>
    <p>Ensuite, c’est l’étape d’empâtage : on mélange le malt concassé avec de l’eau chaude.</p>
    <img src="public/images/maltverseavant.jpeg"/>

    
    <p>C’est là que les enzymes transforment l’amidon en sucres fermentés cibles. </p>
    <img src="public/images/maltverse.png"/>

    <h2>Etape 3</h2>
    <p>Par la suite il faut filtrer ce mélange appelé moût, pour le séparer des résidus solides, qu’on appelle les drêches. </p>
    <img src="public/images/filtrage.png"/>

    <h2>Etape 4</h2>
    <p>Puis vient l’ébullition : on stérilise le moût et on ajoute l'hibiscus, le rooibos et le houblon qui va apporter l’amertume et les arômes en aidant à la conservation. 
    <p>Le moût est refroidit rapidement pour pouvoir ajouter les levures sans les tuer. Celles-ci vont fermenter pendant plusieurs jours afin que les levures transforment les sucres en alcool et en gaz.</p>
    <img src="public/images/ebullition.png"/>

    <h2>Etape 5</h2>
    <p>Le groupe GB a suivi cette étape de près jour après jour en mesurant la densité du moût jusqu’au jour de mise en bouteille où ils ont ajouté un peu de sucre pour relancer une fermentation en bouteille et obtenir une bière naturellement pétillante.</p>
